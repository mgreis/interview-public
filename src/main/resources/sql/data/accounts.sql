DROP TABLE IF EXISTS ACCOUNT;
DROP TABLE IF EXISTS ACCOUNT_KEY;

CREATE TABLE ACCOUNT_KEY(ID BIGINT NOT NULL PRIMARY KEY, ACCOUNT_ID BIGINT NOT NULL);
CREATE TABLE ACCOUNT(ID BIGINT NOT NULL PRIMARY KEY, BALANCE DOUBLE, FIRST_NAME VARCHAR(255), LAST_NAME VARCHAR(255),ACCOUNT_KEY_ID BIGINT NOT NULL ,  FOREIGN KEY (ACCOUNT_KEY_ID) REFERENCES ACCOUNT_KEY(ID))